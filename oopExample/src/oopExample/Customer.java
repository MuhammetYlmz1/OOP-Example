package oopExample;

public class Customer {
	int customerId;
	String customerName;
	String customerSurname;
	String city;
	
	public Customer() {

	}

	public Customer(int id,String customerName,String customerSurname,String city) {
		this.customerId=id;
		this.customerName=customerName;
		this.customerSurname=customerSurname;
		this.city=city;
	}
	
	
}
